num1 = float(input("Ener first number: "))
op = input("Ener first operator: ")
num2 = float(input("Ener second number: "))

if op== "+":
    print(num1+num2)
elif op == "-":
    print(num1-num2)
elif op == "/":
    print(num1/num2)
elif op == "*":
    print(num1 * num2)
else:
    print("Invalid")



